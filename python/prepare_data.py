def to_hex_string(val, bits=8):
    """Converts a signed integer to a 2's complement hex string."""
    val_int = int(val)
    if val_int < 0:
        val_int = (1 << bits) + val_int
    format_str = f"0{bits // 4}x"
    return format(val_int, format_str).upper()

def save_to_hex(filename, data, bits=8):
    """Task D: Generate hex files with rows matching the input data structure for $readmemh."""
    with open(filename, 'w') as f:
        for row in data:
            # Convert each value in the row to hex and join with spaces
            hex_row = " ".join([to_hex_string(val, bits) for val in row])
            f.write(hex_row + '\n')
    print(f"Saved {filename}")

