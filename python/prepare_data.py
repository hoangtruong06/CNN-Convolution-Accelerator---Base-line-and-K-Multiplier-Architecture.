def quantize_to_int8(data):
    """
    Task C: Quantize floating-point array to 8-bit signed integers.
    Determines a scaling factor, rounds, and clips to [-128, 127].
    """
    max_val = np.max(np.abs(data))
    if max_val == 0:
        return data.astype(np.int8), 1.0

    # Scaling factor to fit max_val into 127
    scale = 127.0 / max_val
    quantized = np.round(data * scale)
    quantized = np.clip(quantized, -128, 127).astype(np.int8)

    return quantized, scale

# Quantize input and kernel
